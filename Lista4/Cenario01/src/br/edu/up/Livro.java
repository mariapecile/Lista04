package Lista04.Cenario01;

class Livro {
    String codigo;
    String titulo;
    String[] autores;
    String isbn;
    int ano;

    public Livro(String codigo, String titulo, String[] autores, String isbn, int ano) {
        this.codigo = codigo;
        this.titulo = titulo;
        this.autores = autores;
        this.isbn = isbn;
        this.ano = ano;
    }
}
